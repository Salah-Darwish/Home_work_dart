What does it mean for Dart to be a statically-typed language, and how does it
impact the way variables are used?

Answer:
In Dart, being a statically-typed language means that the type of a variable is known at compile-time rather than at runtime. This means that when you declare a variable, you must specify
its type, and that type cannot change throughout the variable's lifetime. For example, if you declare a variable as an integer, you cannot later assign a string value to it.

 
