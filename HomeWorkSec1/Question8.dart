What is the difference between the num and double data types in Dart?
Answer:
In Dart, the `num` data type is a superclass for both `int` and `double`.
 This means that a variable of type `num` can hold either an integer or a floating-point value. 
 The `double` data type, on the other hand, is specifically used for representing decimal numbers.


